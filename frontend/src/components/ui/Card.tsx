import { forwardRef, type HTMLAttributes } from 'react';
import { cn } from '@/lib/utils';

export interface CardProps extends HTMLAttributes<HTMLDivElement> {
  variant?: 'default' | 'elevated' | 'interactive';
}

export const Card = forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant = 'default', ...props }, ref) => {
    const variants = {
      default: 'bg-charcoal border border-white/8',
      elevated: 'bg-steel border border-white/8',
      interactive:
        'bg-charcoal border border-white/8 hover:border-white/12 hover:bg-steel transition-colors cursor-pointer',
    };

    return (
      <div
        ref={ref}
        className={cn('rounded-md p-4', variants[variant], className)}
        {...props}
      />
    );
  }
);

Card.displayName = 'Card';

export const CardHeader = forwardRef<
  HTMLDivElement,
  HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn('flex flex-col space-y-1 pb-3', className)}
    {...props}
  />
));

CardHeader.displayName = 'CardHeader';

